#!/usr/bin/env node

import fs from "node:fs";
import path from "node:path";
import { spawnSync } from "node:child_process";
import { fileURLToPath } from "node:url";

const GENERATED_MARKER = "Generated by scripts/sync-agent-adapters.mjs";
const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const repoRoot = path.resolve(scriptDir, "..");
const parityConfigPath = path.join(repoRoot, ".agents", "parity.config.json");
const capabilityMapPath = path.join(repoRoot, ".agents", "capability-map.json");

const args = new Set(process.argv.slice(2));
const writeMode = args.has("--write");
const checkMode = args.has("--check");
const machineMode = args.has("--machine");

if (writeMode === checkMode) {
  console.error("Usage: node scripts/sync-agent-adapters.mjs (--write | --check) [--machine]");
  process.exit(2);
}

function normalize(value) {
  return value.split(path.sep).join("/");
}

function normalizeEol(value) {
  return value.replace(/\r\n/g, "\n");
}

function sameText(left, right) {
  return left !== null && normalizeEol(left) === normalizeEol(right);
}

function absolute(relativePath) {
  return path.join(repoRoot, ...relativePath.split("/"));
}

function relative(absolutePath) {
  return normalize(path.relative(repoRoot, absolutePath));
}

function readJson(relativePath) {
  return JSON.parse(fs.readFileSync(absolute(relativePath), "utf8"));
}

function walk(directory) {
  if (!fs.existsSync(directory)) return [];
  const result = [];
  for (const entry of fs.readdirSync(directory, { withFileTypes: true })) {
    const item = path.join(directory, entry.name);
    if (entry.isDirectory()) result.push(...walk(item));
    if (entry.isFile()) result.push(item);
  }
  return result;
}

function unquote(value) {
  const trimmed = value.trim();
  if (trimmed.startsWith('"') && trimmed.endsWith('"')) {
    try {
      return JSON.parse(trimmed);
    } catch {
      return trimmed.slice(1, -1);
    }
  }
  if (trimmed.startsWith("'") && trimmed.endsWith("'")) {
    return trimmed.slice(1, -1).replace(/''/g, "'");
  }
  return trimmed;
}

function parseFrontmatter(content) {
  const match = content.match(/^---\r?\n([\s\S]*?)\r?\n---(?:\r?\n|$)/);
  if (!match) return {};

  const output = {};
  const lines = match[1].split(/\r?\n/);
  for (let index = 0; index < lines.length; index += 1) {
    const keyMatch = lines[index].match(/^([A-Za-z0-9_-]+):\s*(.*)$/);
    if (!keyMatch) continue;
    const [, key, rawValue] = keyMatch;
    if (rawValue === "|" || rawValue === ">") {
      const block = [];
      while (index + 1 < lines.length && (/^\s+/.test(lines[index + 1]) || lines[index + 1] === "")) {
        index += 1;
        block.push(lines[index].trim());
      }
      output[key] = block.join(rawValue === ">" ? " " : "\n").trim();
    } else {
      output[key] = unquote(rawValue);
    }
  }
  return output;
}

function humanize(name) {
  return name
    .replace(/[-_]+/g, " ")
    .replace(/\b\w/g, (letter) => letter.toUpperCase());
}

function slugify(name) {
  return name
    .toLowerCase()
    .replace(/[_/]+/g, "-")
    .replace(/[^a-z0-9-]+/g, "-")
    .replace(/-+/g, "-")
    .replace(/^-|-$/g, "");
}

function cleanDescription(value) {
  return String(value || "")
    .replace(/<example>[\s\S]*$/i, "")
    .replace(/[<>]/g, "")
    .replace(/[`*_#]+/g, "")
    .replace(/\s+/g, " ")
    .trim()
    .slice(0, 700);
}

const parityConfig = JSON.parse(fs.readFileSync(parityConfigPath, "utf8"));
const setupConfig = readJson(".claude/setup.config.json");
const canonicalFiles = walk(absolute(parityConfig.canonicalRoot)).map(relative).sort();
const discoveryErrors = [];

function metadataFor(source, fallbackName) {
  const override = parityConfig.descriptionOverrides[source];
  if (source.endsWith(".json")) {
    const data = readJson(source);
    return {
      title: humanize(fallbackName),
      description: cleanDescription(override || data.description || `Use the ${humanize(fallbackName)} capability.`),
    };
  }

  const content = fs.readFileSync(absolute(source), "utf8");
  const frontmatter = parseFrontmatter(content);
  const heading = content.match(/^#\s+(.+)$/m)?.[1]?.trim();
  const title = heading || humanize(fallbackName);
  const description = cleanDescription(
    override || frontmatter.description || `Use when the task needs the ${title} workflow or guidance.`,
  );
  return { title, description };
}

const requiredPlugins = new Set(setupConfig.plugins.required.map((plugin) => plugin.name));
const optionalPlugins = new Set(setupConfig.plugins.optional.map((plugin) => plugin.name));
const requiredSkills = new Set(setupConfig.skills.required);
const requiredOutputStyles = new Set(setupConfig.outputStyles.required.map((style) => style.name));
const optionalOutputStyles = new Set(setupConfig.outputStyles.optional.map((style) => style.name));
const capabilities = [];
const sourceKeys = new Set();

function addCapability(capability) {
  const sourceKey = `${capability.kind}:${capability.source}:${capability.name}`;
  if (sourceKeys.has(sourceKey)) return;
  sourceKeys.add(sourceKey);
  capabilities.push(capability);
}

function requiredForSource(source) {
  const pluginMatch = source.match(/^\.claude\/plugins\/([^/]+)\//);
  if (pluginMatch) return requiredPlugins.has(pluginMatch[1]);
  const skillMatch = source.match(/^\.claude\/skills\/([^/]+)\//);
  if (skillMatch) {
    return requiredSkills.has(skillMatch[1]) || requiredOutputStyles.has(skillMatch[1]);
  }
  return true;
}

function addSkill(name, source, origin, required = requiredForSource(source)) {
  const finalName = slugify(parityConfig.nameOverrides[source] || name);
  const metadata = metadataFor(source, finalName);
  addCapability({
    kind: "skill",
    name: finalName,
    displayName: metadata.title,
    source,
    target: `.agents/skills/${finalName}/SKILL.md`,
    adapter: "canonical-wrapper",
    origin,
    required,
    description: metadata.description,
  });
}

for (const dependency of parityConfig.nativeDependencies) {
  const marketplaceSuffix = dependency.marketplace ? `@${dependency.marketplace}` : "";
  addCapability({
    kind: "native-plugin",
    name: dependency.plugin,
    source: ".claude/setup.config.json",
    target: `plugin:${dependency.plugin}${marketplaceSuffix}`,
    adapter: "machine-plugin",
    origin: "external-plugin",
    required: dependency.required,
    minimumVersion: dependency.minimumVersion,
    description: dependency.note,
  });

  for (const sourceAdapter of dependency.sourceAdapters) {
    const coveredFiles = canonicalFiles.filter(
      (source) => source.startsWith(sourceAdapter.prefix) && source.endsWith(".md"),
    );
    if (coveredFiles.length === 0) {
      discoveryErrors.push(`Native mapping ${dependency.id} covers no files: ${sourceAdapter.prefix}`);
    }
    for (const source of coveredFiles) {
      const sourceName = path.posix.basename(source, ".md");
      const targetName = `${sourceAdapter.targetPrefix}${sourceName}`;
      addCapability({
        kind: source.includes("/commands/") ? "native-command" : "native-skill",
        name: targetName,
        source,
        target: `plugin:${dependency.plugin}${marketplaceSuffix}#${targetName}`,
        adapter: "native-plugin",
        origin: dependency.id,
        required: dependency.required,
        description: `Covered by the Codex-native ${dependency.plugin} plugin; no project wrapper is generated.`,
      });
    }
  }
}

const nativeCoveredSources = new Set(
  capabilities.filter((capability) => capability.adapter === "native-plugin").map((capability) => capability.source),
);

for (const source of canonicalFiles) {
  const rootSkill = source.match(/^\.claude\/skills\/([^/]+)\/SKILL\.md$/);
  if (rootSkill) addSkill(rootSkill[1], source, "project-skill", requiredSkills.has(rootSkill[1]));

  const looseSkill = source.match(/^\.claude\/skills\/([^/]+)\.md$/);
  if (looseSkill && looseSkill[1].toLowerCase() !== "readme") {
    addSkill(looseSkill[1], source, "project-skill", requiredSkills.has(looseSkill[1]));
  }

  const projectCommand = source.match(/^\.claude\/commands\/(.+)\.md$/);
  if (projectCommand && !source.endsWith("/README.md") && !nativeCoveredSources.has(source)) {
    addSkill(projectCommand[1].replaceAll("/", "-"), source, "project-command", true);
  }

  const pluginCommand = source.match(/^\.claude\/plugins\/([^/]+)\/commands\/(.+)\.md$/);
  if (pluginCommand && !source.endsWith("/README.md")) {
    addSkill(
      path.posix.basename(pluginCommand[2]),
      source,
      "plugin-command",
      requiredPlugins.has(pluginCommand[1]),
    );
  }

  const pluginSkill = source.match(/^\.claude\/plugins\/([^/]+)\/skills\/(.+)\/SKILL\.md$/);
  if (pluginSkill) {
    addSkill(
      path.posix.basename(pluginSkill[2]),
      source,
      "plugin-skill",
      requiredPlugins.has(pluginSkill[1]),
    );
  }

  const pluginAgent = source.match(/^\.claude\/plugins\/([^/]+)\/agents\/(.+)\.md$/);
  if (pluginAgent) {
    const baseName = path.posix.basename(pluginAgent[2]);
    const name = slugify(parityConfig.nameOverrides[source] || `${pluginAgent[1]}-${baseName}`);
    const metadata = metadataFor(source, name);
    addCapability({
      kind: "agent",
      name,
      displayName: metadata.title,
      source,
      target: `.codex/agents/${name}.toml`,
      adapter: "canonical-agent",
      origin: pluginAgent[1],
      required: requiredPlugins.has(pluginAgent[1]),
      description: metadata.description,
    });
  }
}

for (const style of [...setupConfig.outputStyles.required, ...setupConfig.outputStyles.optional]) {
  const source = `${style.path}/README.md`;
  if (!fs.existsSync(absolute(source))) {
    discoveryErrors.push(`Output style source missing: ${source}`);
    continue;
  }
  addSkill(style.name, source, "output-style", requiredOutputStyles.has(style.name));
}

for (const syntheticSkill of parityConfig.syntheticSkills) {
  if (!fs.existsSync(absolute(syntheticSkill.source))) {
    discoveryErrors.push(`Synthetic skill source missing: ${syntheticSkill.source}`);
    continue;
  }
  addSkill(
    syntheticSkill.name,
    syntheticSkill.source,
    syntheticSkill.origin,
    requiredSkills.has(syntheticSkill.name),
  );
}

for (const hook of parityConfig.hookAdapters) {
  if (!fs.existsSync(absolute(hook.source))) discoveryErrors.push(`Hook source missing: ${hook.source}`);
  addCapability({
    kind: "hook",
    name: hook.name,
    source: hook.source,
    target: hook.target,
    adapter: "native-hook-bridge",
    origin: "hook",
    required: requiredForSource(hook.source),
    events: hook.events,
    description: `Codex bridge for ${hook.events.join(", ")} from the canonical Claude hook.`,
  });
}

const actualHookSources = canonicalFiles.filter((source) => /\/hooks\/hooks\.json$/.test(source));
const mappedHookSources = new Set(parityConfig.hookAdapters.map((hook) => hook.source));
for (const source of actualHookSources) {
  if (!mappedHookSources.has(source)) discoveryErrors.push(`Unmapped Claude hook manifest: ${source}`);
}

const pluginManifestSources = canonicalFiles.filter((source) => /\/\.claude-plugin\/plugin\.json$/.test(source));
for (const source of pluginManifestSources) {
  const manifest = readJson(source);
  const pluginName = manifest.name || path.posix.basename(path.posix.dirname(path.posix.dirname(source)));
  const required =
    requiredPlugins.has(pluginName) || requiredOutputStyles.has(pluginName) || requiredSkills.has(pluginName);
  addCapability({
    kind: "plugin",
    name: pluginName,
    source,
    target: `expanded:codex/${pluginName}`,
    adapter: "expanded-components",
    origin: source.startsWith(".claude/skills/") ? "skills-dir-plugin" : "vendored-plugin",
    required,
    description: cleanDescription(manifest.description || `Claude plugin expanded into native Codex components.`),
  });
}

addCapability({
  kind: "marketplace",
  name: "vibe-code-starter",
  source: ".claude-plugin/marketplace.json",
  target: ".agents/capability-map.json",
  adapter: "expanded-components",
  origin: "local-marketplace",
  required: true,
  description: "Claude local marketplace whose vendored components are expanded into project-scoped Codex adapters.",
});

const externalPluginNames = [
  ...setupConfig.plugins.required.filter((plugin) => plugin.vendored === null),
  ...setupConfig.plugins.optional.filter((plugin) => plugin.vendored === null),
].map((plugin) => plugin.name);
const dependencyNames = new Set(parityConfig.nativeDependencies.map((dependency) => dependency.id));
for (const name of externalPluginNames) {
  if (!dependencyNames.has(name)) discoveryErrors.push(`External plugin has no Codex dependency mapping: ${name}`);
}

const skillNames = capabilities.filter((capability) => capability.kind === "skill").map((capability) => capability.name);
const duplicateSkills = skillNames.filter((name, index) => skillNames.indexOf(name) !== index);
if (duplicateSkills.length > 0) discoveryErrors.push(`Duplicate Codex skill names: ${[...new Set(duplicateSkills)].join(", ")}`);

for (const name of [...requiredSkills, ...requiredOutputStyles]) {
  if (!skillNames.includes(name)) discoveryErrors.push(`Required Codex skill adapter missing: ${name}`);
}

for (const capability of capabilities.filter((item) => item.kind === "skill" || item.kind === "agent")) {
  if (!/^[a-z0-9-]+$/.test(capability.name) || capability.name.length > 64) {
    discoveryErrors.push(`Invalid Codex adapter name: ${capability.name}`);
  }
}

capabilities.sort((left, right) =>
  `${left.kind}\0${left.name}\0${left.source}`.localeCompare(`${right.kind}\0${right.name}\0${right.source}`),
);

for (const capability of capabilities) {
  capability.id = `${capability.kind}:${capability.name}:${capability.source}`;
}

const countByKind = Object.fromEntries(
  [...new Set(capabilities.map((capability) => capability.kind))]
    .sort()
    .map((kind) => [kind, capabilities.filter((capability) => capability.kind === kind).length]),
);

const capabilityMap = {
  schemaVersion: parityConfig.schemaVersion,
  canonicalRoot: parityConfig.canonicalRoot,
  generatedBy: parityConfig.generatedBy,
  policy: {
    canonicalBehavior: ".claude",
    codexSkills: ".agents/skills",
    codexAgents: ".codex/agents",
    codexHooks: ".codex/hooks.json",
    driftPolicy: "fail",
  },
  nativeDependencies: parityConfig.nativeDependencies,
  summary: {
    total: capabilities.length,
    byKind: countByKind,
  },
  capabilities,
};

function skillDescription(capability) {
  const explicit = capability.origin.endsWith("command");
  const prefix = explicit
    ? `Use when the user explicitly invokes $${capability.name} or asks to run that workflow. `
    : "";
  return `${prefix}${capability.description}`.slice(0, 1000);
}

function renderSkill(capability) {
  const special = parityConfig.specialInstructions[capability.source];
  return `---\nname: ${capability.name}\ndescription: ${JSON.stringify(skillDescription(capability))}\n---\n\n<!-- ${GENERATED_MARKER}; do not edit directly. -->\n\n# ${capability.displayName || humanize(capability.name)}\n\n+This is the Codex adapter for the canonical Claude capability at\n+\`${capability.source}\`.\n+\n+When this skill is selected:\n+\n+1. Resolve the repository root with \`git rev-parse --show-toplevel\`.\n+2. Read \`<repo>/.agents/CODEX_COMPATIBILITY.md\` completely.\n+3. Read \`<repo>/${capability.source}\` completely, including any referenced files needed for the task.\n+4. Follow the same workflow order, approval gates, safety rules, and verification requirements.\n+5. Treat text after \`$${capability.name}\` as the canonical command's arguments when applicable.\n+6. Translate only platform surfaces; do not change canonical behavior or silently skip steps.\n+7. Do not edit the canonical source merely to make Codex execute it.\n+${special ? `\n+## Codex bridge\n+\n+${special}\n+` : ""}`.replace(/^\+/gm, "");
}

function tomlString(value) {
  return JSON.stringify(cleanDescription(value));
}

function renderAgent(capability) {
  return `# ${GENERATED_MARKER}; do not edit directly.\n+name = ${tomlString(capability.name)}\n+description = ${tomlString(capability.description)}\n+developer_instructions = \"\"\"\n+This is a Codex adapter for the canonical Claude agent at ${capability.source}.\n+\n+Before acting:\n+1. Resolve the repository root with git rev-parse --show-toplevel.\n+2. Read <repo>/.agents/CODEX_COMPATIBILITY.md completely.\n+3. Read <repo>/${capability.source} completely and follow its role, boundaries, process, and output contract.\n+4. Resolve linked files relative to the canonical plugin directory.\n+5. Translate Claude tool names through the compatibility contract while preserving tool limits and safety intent.\n+6. Do not edit the canonical source unless the user explicitly requests a behavior change.\n+\"\"\"\n+`.replace(/^\+/gm, "");
}

const expectedGeneratedFiles = new Map();
for (const capability of capabilities) {
  if (capability.kind === "skill") expectedGeneratedFiles.set(capability.target, renderSkill(capability));
  if (capability.kind === "agent") expectedGeneratedFiles.set(capability.target, renderAgent(capability));
}

const expectedMapContent = `${JSON.stringify(capabilityMap, null, 2)}\n`;

function writeFile(relativePath, content) {
  const target = absolute(relativePath);
  fs.mkdirSync(path.dirname(target), { recursive: true });
  const current = fs.existsSync(target) ? fs.readFileSync(target, "utf8") : null;
  if (!sameText(current, content)) {
    fs.writeFileSync(target, content, "utf8");
    console.log(`updated ${relativePath}`);
  }
}

const checkErrors = [...discoveryErrors];

if (writeMode && checkErrors.length === 0) {
  writeFile(".agents/capability-map.json", expectedMapContent);
  for (const [target, content] of expectedGeneratedFiles) writeFile(target, content);
}

if (checkMode) {
  const currentMap = fs.existsSync(capabilityMapPath) ? fs.readFileSync(capabilityMapPath, "utf8") : null;
  if (!sameText(currentMap, expectedMapContent)) {
    checkErrors.push("Capability map is stale; run node scripts/sync-agent-adapters.mjs --write");
  }

  for (const [target, content] of expectedGeneratedFiles) {
    if (!fs.existsSync(absolute(target))) {
      checkErrors.push(`Generated adapter missing: ${target}`);
    } else if (!sameText(fs.readFileSync(absolute(target), "utf8"), content)) {
      checkErrors.push(`Generated adapter is stale or hand-edited: ${target}`);
    }
  }

  const generatedRoots = [".agents/skills", ".codex/agents"];
  for (const root of generatedRoots) {
    for (const file of walk(absolute(root))) {
      const source = fs.readFileSync(file, "utf8");
      const item = relative(file);
      if (source.includes(GENERATED_MARKER) && !expectedGeneratedFiles.has(item)) {
        checkErrors.push(`Stale generated adapter not in capability map: ${item}`);
      }
    }
  }

  for (const file of parityConfig.requiredCodexFiles) {
    if (!fs.existsSync(absolute(file))) checkErrors.push(`Required Codex file missing: ${file}`);
  }
  for (const hook of parityConfig.hookAdapters) {
    if (!fs.existsSync(absolute(hook.target))) checkErrors.push(`Hook adapter missing: ${hook.target}`);
  }
}

function compareVersions(actual, minimum) {
  const parse = (value) => (value.match(/\d+/g) || []).slice(0, 3).map(Number);
  const left = parse(actual);
  const right = parse(minimum);
  for (let index = 0; index < 3; index += 1) {
    const difference = (left[index] || 0) - (right[index] || 0);
    if (difference !== 0) return difference;
  }
  return 0;
}

if (machineMode && checkErrors.length === 0) {
  const result = spawnSync("codex plugin list", {
    cwd: repoRoot,
    encoding: "utf8",
    shell: true,
    windowsHide: true,
  });
  if (result.status !== 0) {
    checkErrors.push(`Unable to inspect Codex plugins: ${(result.stderr || result.error || "unknown error").toString().trim()}`);
  } else {
    const output = result.stdout.replace(/\u001b\[[0-9;]*m/g, "");
    const rows = output.split(/\r?\n/).map((line) => line.trim()).filter(Boolean);
    for (const dependency of parityConfig.nativeDependencies) {
      const exactName = dependency.marketplace ? `${dependency.plugin}@${dependency.marketplace}` : null;
      const row = rows.find((line) => {
        const firstColumn = line.split(/\s{2,}/)[0];
        return exactName ? firstColumn === exactName : firstColumn?.startsWith(`${dependency.plugin}@`);
      });
      const columns = row?.split(/\s{2,}/) || [];
      const enabled = columns[1] === "installed, enabled";
      const version = enabled ? columns[2] : null;
      const versionOkay = !dependency.minimumVersion || (version && compareVersions(version, dependency.minimumVersion) >= 0);

      if (dependency.required && (!row || !enabled || !versionOkay)) {
        checkErrors.push(
          `Required Codex plugin ${exactName || dependency.plugin} is missing, disabled, or older than ${dependency.minimumVersion || "the required version"}`,
        );
      } else if (!dependency.required && (!row || !enabled)) {
        console.warn(`optional plugin unavailable: ${dependency.plugin} (parity needed only when opted in)`);
      } else if (enabled) {
        console.log(`machine plugin ok: ${columns[0]} ${version || "version unknown"}`);
        const expectedNativeSkills = capabilities
          .filter((capability) => capability.origin === dependency.id && capability.adapter === "native-plugin")
          .map((capability) => capability.name);
        if (expectedNativeSkills.length > 0) {
          const pluginRoot = columns[3];
          const manifestPath = pluginRoot && path.join(pluginRoot, ".codex-plugin", "plugin.json");
          if (!manifestPath || !fs.existsSync(manifestPath)) {
            checkErrors.push(`Cannot inspect native skill inventory for ${columns[0]}`);
          } else {
            const manifest = JSON.parse(fs.readFileSync(manifestPath, "utf8"));
            const configuredSkillRoots = Array.isArray(manifest.skills)
              ? manifest.skills
              : [manifest.skills || "./skills/"];
            const actualNativeSkills = new Set();
            for (const configuredRoot of configuredSkillRoots) {
              const skillRoot = path.resolve(pluginRoot, configuredRoot);
              for (const skillFile of walk(skillRoot).filter((file) => path.basename(file) === "SKILL.md")) {
                const frontmatter = parseFrontmatter(fs.readFileSync(skillFile, "utf8"));
                if (frontmatter.name) actualNativeSkills.add(frontmatter.name);
              }
            }
            const missingNativeSkills = expectedNativeSkills.filter((name) => !actualNativeSkills.has(name));
            if (missingNativeSkills.length > 0) {
              checkErrors.push(`Native ${dependency.plugin} skills missing: ${missingNativeSkills.join(", ")}`);
            } else {
              console.log(`machine skill parity ok: ${dependency.plugin} (${expectedNativeSkills.length} mapped skills)`);
            }
          }
        }
      }
    }
  }
}

if (checkErrors.length > 0) {
  for (const error of checkErrors) console.error(`ERROR: ${error}`);
  process.exit(1);
}

console.log(
  `${writeMode ? "sync complete" : "parity check passed"}: ${capabilities.length} capabilities, ${expectedGeneratedFiles.size} generated adapters`,
);
